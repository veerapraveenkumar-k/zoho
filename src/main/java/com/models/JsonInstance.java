package com.models;

public class JsonInstance {
	private int instanceId;
	private String fileName;
	
	//getters
	
	public int getId() {
		return instanceId;
	}
	
	public String getFileName() {
		return fileName;
	}
	
	//setters
	
	public void setId(int id) {
		this.instanceId = id;
	}
	
	public void setFileName(String name) {
		this.fileName = name;
	}
}
	
